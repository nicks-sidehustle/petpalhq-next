#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { ROOT } from '../lib/content-audit.mjs';

const packagePath = path.join(ROOT, 'package.json');
const packageJson = JSON.parse(fs.readFileSync(packagePath, 'utf8'));
const reportsDir = path.join(ROOT, 'docs/refresh/reports');
fs.mkdirSync(reportsDir, { recursive: true });

function extractLocalTargets(command) {
  const targets = [];
  const patterns = [
    /(?:^|\s)(?:node|tsx|ts-node|bash|sh)\s+((?:\.\/)?scripts\/[^\s;&|]+)/g,
    /(?:^|\s)(\.\/scripts\/[^\s;&|]+)/g,
  ];
  for (const pattern of patterns) {
    let match;
    while ((match = pattern.exec(command))) {
      targets.push(match[1].replace(/^\.\//, ''));
    }
  }
  return [...new Set(targets)].sort();
}

const rows = Object.entries(packageJson.scripts ?? {}).flatMap(([name, command]) => {
  const targets = extractLocalTargets(command);
  return targets.map((target) => ({
    name,
    command,
    target,
    exists: fs.existsSync(path.join(ROOT, target)),
    recommendation: fs.existsSync(path.join(ROOT, target)) ? 'keep' : 'restore, replace, or remove stale script target',
  }));
}).sort((a, b) => a.name.localeCompare(b.name) || a.target.localeCompare(b.target));

const missing = rows.filter((row) => !row.exists);
const json = { scriptTargetCount: rows.length, missingCount: missing.length, rows };
const markdown = `# Package Script Health Audit\n\nGenerated by \`node scripts/refresh/audit-package-scripts.mjs\`.\n\n## Summary\n\n- Local script targets referenced: ${rows.length}\n- Missing local script targets: ${missing.length}\n\n## Missing targets\n\n${missing.map((row) => `- \`${row.name}\`: \`${row.target}\` — ${row.recommendation}`).join('\n') || '- None'}\n\n## Existing local targets\n\n${rows.filter((row) => row.exists).map((row) => `- \`${row.name}\`: \`${row.target}\``).join('\n') || '- None'}\n`;

fs.writeFileSync(path.join(reportsDir, 'package-script-health.json'), `${JSON.stringify(json, null, 2)}\n`);
fs.writeFileSync(path.join(reportsDir, 'package-script-health.md'), markdown);
console.log(`Wrote docs/refresh/reports/package-script-health.md and package-script-health.json`);
console.log(`Missing local script targets: ${missing.length}`);
if (missing.length) process.exitCode = 0;
